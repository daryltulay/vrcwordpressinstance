				<h2>ARCH. Ma. Concepcion Villegas - Roxas, PhD</h2>
				<p> 
<strong>Ma. Concepcion V. Roxas</strong>, founding General Manager of Villegas-Roxas Consultants (VRC), works on novel and implementable design. Her recent entry <strong>Sagay: Tubbataha Marine Life Sanctuary, Palawan, Philippines</strong> garnered recognition & honorable mention in ‘Innovative Minds 2013: Virtual Context’ – an International Architectural Design Competition hosted by Gurroo.com. </p>
				
<p class="padify">Collaborating with VRC designers, Roxas values the talents and vocation in architecture, master planning and interior design.  She visualizes <strong>designs that push the boundaries of what may seem possible—</strong>from built designs to theoretical and academic works.</p>
				
<p class="padify marginify">With the belief that architecture has the power to influence the way any society views itself and functions, Roxas recognizes excellence in all areas of design with the edge in the natural systems, landscaping and urbanism combined with technologies. </p>
				
